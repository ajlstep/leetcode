package romantointeger

import (
	"testing"

	"github.com/stretchr/testify/require"
)

func TestRomanToInt2(t *testing.T) {
	require.Equal(t, romanToInt2("MCCXXIV"), 1224, "MCCXXIV|1224")
	require.Equal(t, romanToInt2("CCXL"), 240, "CCXL|240")
	require.Equal(t, romanToInt2("MCCCXXXVIII"), 1338, "MCCCXXXVIII|1338")
	require.Equal(t, romanToInt2("CLXVIII"), 168, "CLXVIII|168")
	require.Equal(t, romanToInt2("CCXV"), 215, "CCXV|215")
	require.Equal(t, romanToInt2("DLXXV"), 575, "DLXXV|575")
	require.Equal(t, romanToInt2("MMCDLXXXVIII"), 2488, "MMCDLXXXVIII|2488")
	require.Equal(t, romanToInt2("CCCXV"), 315, "CCCXV|315")
	require.Equal(t, romanToInt2("MMCDLXXXVII"), 2487, "MMCDLXXXVII|2487")
	require.Equal(t, romanToInt2("DXCIII"), 593, "DXCIII|593")
	require.Equal(t, romanToInt2("CCLI"), 251, "CCLI|251")
	require.Equal(t, romanToInt2("MCXXVIII"), 1128, "MCXXVIII|1128")
	require.Equal(t, romanToInt2("CCXLVIII"), 248, "CCXLVIII|248")
	require.Equal(t, romanToInt2("MMCMIX"), 2909, "MMCMIX|2909")
	require.Equal(t, romanToInt2("MCCCXCIV"), 1394, "MCCCXCIV|1394")
	require.Equal(t, romanToInt2("DCLXXV"), 675, "DCLXXV|675")
	require.Equal(t, romanToInt2("MCCXXXVIII"), 1238, "MCCXXXVIII|1238")
	require.Equal(t, romanToInt2("CCLXVI"), 266, "CCLXVI|266")
	require.Equal(t, romanToInt2("MMCMLXVI"), 2966, "MMCMLXVI|2966")
	require.Equal(t, romanToInt2("MCCXLVIII"), 1248, "MCCXLVIII|1248")
	require.Equal(t, romanToInt2("CCXCVI"), 296, "CCXCVI|296")
	require.Equal(t, romanToInt2("MDCCXXXIII"), 1733, "MDCCXXXIII|1733")
	require.Equal(t, romanToInt2("CMLXVIII"), 968, "CMLXVIII|968")
	require.Equal(t, romanToInt2("MMMCMLXVII"), 3967, "MMMCMLXVII|3967")
	require.Equal(t, romanToInt2("CCCXLIII"), 343, "CCCXLIII|343")
	require.Equal(t, romanToInt2("MCDLXXXV"), 1485, "MCDLXXXV|1485")
	require.Equal(t, romanToInt2("CCXC"), 290, "CCXC|290")
	require.Equal(t, romanToInt2("MMCDXC"), 2490, "MMCDXC|2490")
	require.Equal(t, romanToInt2("MMCMVIII"), 2908, "MMCMVIII|2908")
	require.Equal(t, romanToInt2("CCVII"), 207, "CCVII|207")
	require.Equal(t, romanToInt2("MMDCCCXC"), 2890, "MMDCCCXC|2890")
	require.Equal(t, romanToInt2("CCLXXXVI"), 286, "CCLXXXVI|286")
	require.Equal(t, romanToInt2("MMMCCLXVII"), 3267, "MMMCCLXVII|3267")
	require.Equal(t, romanToInt2("DCXXXVI"), 636, "DCXXXVI|636")
	require.Equal(t, romanToInt2("MMCX"), 2110, "MMCX|2110")
	require.Equal(t, romanToInt2("MCCCXIV"), 1314, "MCCCXIV|1314")
	require.Equal(t, romanToInt2("CCXXXVIII"), 238, "CCXXXVIII|238")
	require.Equal(t, romanToInt2("MMMCMLXIV"), 3964, "MMMCMLXIV|3964")
	require.Equal(t, romanToInt2("DCCCXCIII"), 893, "DCCCXCIII|893")
	require.Equal(t, romanToInt2("MMCMXIII"), 2913, "MMCMXIII|2913")
	require.Equal(t, romanToInt2("MCCCXXVII"), 1327, "MCCCXXVII|1327")
	require.Equal(t, romanToInt2("CCXXXVII"), 237, "CCXXXVII|237")
	require.Equal(t, romanToInt2("MMDCXXX"), 2630, "MMDCXXX|2630")
	require.Equal(t, romanToInt2("MCCCXXV"), 1325, "MCCCXXV|1325")
	require.Equal(t, romanToInt2("CCCXLV"), 345, "CCCXLV|345")
	require.Equal(t, romanToInt2("MCDLXVIII"), 1468, "MCDLXVIII|1468")
	require.Equal(t, romanToInt2("DCCCXV"), 815, "DCCCXV|815")
	require.Equal(t, romanToInt2("MMCMXVIII"), 2918, "MMCMXVIII|2918")
	require.Equal(t, romanToInt2("CMLVIII"), 958, "CMLVIII|958")
	require.Equal(t, romanToInt2("MMCDXVIII"), 2418, "MMCDXVIII|2418")
}
